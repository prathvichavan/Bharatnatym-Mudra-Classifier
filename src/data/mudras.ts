// Combined array for gallery view
export const allMudras: Mudra[] = [...oneHandMudras, ...twoHandMudras];

// Helper functions
export const getMudraById = (id: string): Mudra | undefined => {
  return allMudras.find(mudra => mudra.id === id);
};

export const getMudrasByType = (type: 'one-hand' | 'two-hand'): Mudra[] => {
  return allMudras.filter(mudra => mudra.type === type);
};

export const getMudrasByCategory = (category: 'basic' | 'intermediate' | 'advanced'): Mudra[] => {
  return allMudras.filter(mudra => mudra.category === category);
};

export const searchMudras = (query: string): Mudra[] => {
  const lowerQuery = query.toLowerCase();
  return allMudras.filter(mudra => 
    mudra.nameEnglish.toLowerCase().includes(lowerQuery) ||
    mudra.nameSanskrit.toLowerCase().includes(lowerQuery) ||
    mudra.shortDescription.toLowerCase().includes(lowerQuery)
  );
};
